package generationProcedurale;

public class Center {
    private int centerX;
    private int centerY;


    public Center(int centerX, int centerY) {
        this.centerX = centerX;
        this.centerY = centerY;
    }

    public int getCenterX() {
        return centerX;
    }

    public int getCenterY() {
        return centerY;
    }

}